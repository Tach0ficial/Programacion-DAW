/**
 * Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando
 * un bucle while.
 *
 * @author Carlos Hidalgo Risco
 */

public class Ejercicio_5 { // Clase principal
    public static void main(String[] args) {
        int i = 320; 

        while (i >=160 ){//realiza el bucle mientras i sea mayor o igual a 160
            System.out.println(i);
            i-=20;//i se va decrementando de 20 en 20.
        }
       

      
    }
}