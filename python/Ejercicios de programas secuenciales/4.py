# Pido los dos numeros.
numero_1 = float(input("Dime un número: "))
numero_2 = float(input("Dime un número: "))

# Calculo la suma, resta, división y multiplicación de ambos.
suma = numero_1 + numero_2
resta = numero_1 - numero_2
division = numero_1 / numero_2
multiplicacion = numero_1 * numero_2

# Imprimo el resultado la suma, resta, división y multiplicación de ambos.
print("El resutado de sumar",numero_1,"y",numero_2," es:",suma)
print("El resutado de restar",numero_1,"y",numero_2," es:",resta) 
print("El resutado de dividir",numero_1,"y",numero_2," es:",division)
print("El resutado de multiplicar",numero_1,"y",numero_2," es:",multiplicacion) 